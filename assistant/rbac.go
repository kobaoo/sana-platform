package assistant

import (
	"fmt"
	"strings"

	"encore.app/auth/authhandler"
)

// rbacDirective возвращает краткую директиву контроля доступа для текущей роли.
//
// Почему директива инжектируется в user message, а не только в system_instruction:
//
//   - system_instruction (context.go) содержит СТАТИЧЕСКИЕ правила — они описывают
//     общий принцип RBAC и применяются ко всем сессиям без исключения.
//
//   - rbacDirective (этот файл) — ДИНАМИЧЕСКАЯ директива per-request.
//     Она явно сообщает модели: "прямо сейчас передо мной пользователь роли X".
//     Дублирование намеренно: LLM лучше соблюдает ограничения, когда они
//     повторяются и в system_instruction, и в начале каждого user message.
//
// Стратегия формулировок — allowlist, не denylist:
//
//	Для SA и EMP мы перечисляем ЧТО РАЗРЕШЕНО (явный список).
//	Для ADM и HR добавляем ЗАПРЕЩЕНО — потому что их доступ шире EMP
//	и граничные случаи важно назвать явно.
//	Такой подход точнее ограничивает LLM, чем только список запретов.
func rbacDirective(role authhandler.UserRole) string {
	switch role {

	case authhandler.RoleSA:
		// SA — неограниченный доступ. Не добавляем запреты, чтобы не вводить
		// модель в замешательство ненужными инструкциями.
		return "[РОЛЬ ТЕКУЩЕГО ПОЛЬЗОВАТЕЛЯ: СУПЕР-АДМИНИСТРАТОР (SA)]\n" +
			"Пользователь имеет полный доступ ко всем функциям и данным платформы: " +
			"управление клиентами (/clients), администраторами (/admins), ДЗО (/dzo), " +
			"сотрудниками (/employees), договорами (/contracts), поставщиками (/suppliers), " +
			"SCORM-курсами (/scorm), мероприятиями (/events), заявками (/requests), " +
			"сертификатами (/certificates), категориями (/categories), аналитикой."

	case authhandler.RoleADM:
		return "[РОЛЬ ТЕКУЩЕГО ПОЛЬЗОВАТЕЛЯ: АДМИНИСТРАТОР КЛИЕНТА (ADM)]\n" +
			"Доступные области: сотрудники (/employees), ДЗО (/dzo), " +
			"договоры (/contracts), поставщики (/suppliers), SCORM-курсы (/scorm), " +
			"мероприятия (/events), заявки (/requests), сертификаты (/certificates), " +
			"категории (/categories), организации (/organizations), аналитика своего клиента.\n" +
			"ЗАПРЕЩЕНО отвечать на вопросы о: управлении другими клиентами (/clients), " +
			"управлении администраторами платформы (/admins), глобальной аналитике.\n" +
			"При запросе запрещённой информации ответь: " +
			"\"У вас недостаточно прав для доступа к этой функции.\""

	case authhandler.RoleHR:
		return "[РОЛЬ ТЕКУЩЕГО ПОЛЬЗОВАТЕЛЯ: HR ДЗО (HR)]\n" +
			"Доступные области: заявки на обучение (/requests), " +
			"сертификаты сотрудников своего ДЗО (/certificates), " +
			"мероприятия (/events — просмотр и регистрация), " +
			"внешние курсы (/external-courses), " +
			"карточки сотрудников своего ДЗО (/employees и /employees/[id]).\n" +
			"ЗАПРЕЩЕНО отвечать на вопросы о: договорах с поставщиками (/contracts), " +
			"финансах и бюджетах клиента, загрузке SCORM-курсов, управлении поставщиками (/suppliers), " +
			"настройках ADM-уровня, данных сотрудников других ДЗО.\n" +
			"При запросе запрещённой информации ответь: " +
			"\"У вас недостаточно прав для доступа к этой функции.\""

	default: // RoleEMP — минимальный доступ.
		// Используем явный allowlist (что разрешено), а не только denylist.
		// Это точнее ограничивает LLM: модель не будет угадывать границы.
		return "[РОЛЬ ТЕКУЩЕГО ПОЛЬЗОВАТЕЛЯ: СОТРУДНИК (EMP)]\n" +
			"Пользователь имеет доступ ТОЛЬКО к следующим разделам:\n" +
			"  - /profile — личный кабинет (просмотр и редактирование своих данных)\n" +
			"  - /scorm и /scorm/[id]/player — просмотр и прохождение SCORM-курсов\n" +
			"  - /events — просмотр мероприятий и регистрация\n" +
			"  - /external-courses и /external-courses/[id] — просмотр внешних курсов\n" +
			"  - /certificates — только свои сертификаты\n" +
			"  - /requests — только свои заявки на обучение\n" +
			"Любые другие разделы и функции недоступны.\n" +
			"При запросе о договорах, бюджетах, управлении сотрудниками, " +
			"HR-процессах, поставщиках, аналитике компании или данных других сотрудников ответь: " +
			"\"У вас недостаточно прав для доступа к этой функции.\""
	}
}

// BuildUserMessage формирует итоговое сообщение пользователя для отправки в LLM.
//
// Экспортируется (заглавная B) для использования в интеграционных тестах
// и возможного переиспользования в других частях пакета.
//
// Структура сообщения (порядок важен — LLM лучше следует
// инструкциям в начале сообщения):
//  1. RBAC-директива для текущей роли (динамическая, per-request)
//  2. Идентификатор сессии (Keycloak User ID — для трассировки инцидентов)
//  3. Живой контекст платформы (актуальные курсы/мероприятия из БД, опционально)
//  4. Вопрос пользователя (отделён явным маркером [ВОПРОС ПОЛЬЗОВАТЕЛЯ])
func BuildUserMessage(
	role authhandler.UserRole,
	userID string,
	liveContext string,
	message string,
) string {
	var b strings.Builder

	// Блок 1: RBAC — применяется к данной сессии.
	// Идёт первым, чтобы иметь наивысший приоритет в контексте LLM.
	b.WriteString(rbacDirective(role))
	b.WriteString("\n\n")

	// Блок 2: Идентификатор сессии.
	// Не используется в логике ответа, но полезен для трассировки
	// при расследовании инцидентов.
	fmt.Fprintf(&b, "[ID сессии: %s]\n\n", userID)

	// Блок 3: Живой контекст (опционально).
	// Актуальные мероприятия/курсы из БД. Добавляется только если не пустой,
	// чтобы не засорять промпт лишними секциями.
	if liveContext != "" {
		b.WriteString("[АКТУАЛЬНЫЙ КОНТЕКСТ ПЛАТФОРМЫ]\n")
		b.WriteString(liveContext)
		b.WriteString("\n\n")
	}

	// Блок 4: Вопрос пользователя.
	// Явный маркер гарантирует, что LLM не перепутает вопрос с инструкцией.
	b.WriteString("[ВОПРОС ПОЛЬЗОВАТЕЛЯ]\n")
	b.WriteString(message)

	return b.String()
}
